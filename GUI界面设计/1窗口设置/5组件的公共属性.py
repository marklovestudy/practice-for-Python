'''
组件的公共属性10个
1.foreground或fg             设置组件中文字的颜色
2.background或bg             设置组件的背景颜色
3.width                     设置组件宽度
4.height                    设置组件的高度
5.anchor                    设置内容输出位置，默认为center(上下左右居中)，另可选：八方(e,s,w,n,nw,ne,sw,se)
6.padx                      组件的水平间距
7.pady                      组件的垂直间距
8.font                      组件的文件样式
9.relief                    组件的边框样式
10.cursor                   鼠标悬停在组件上时的样式

8font标签中可以设置文字相关属性
    1size                   设置字号，单位px
    2family                 设置字体，例如Times
    3weight                 设置文字粗细，如bold
    4slant                  设置斜体，如italic
    5underline              添加下划线，值为True或False
    6overstrike             添加删除线，值为True或False
'''